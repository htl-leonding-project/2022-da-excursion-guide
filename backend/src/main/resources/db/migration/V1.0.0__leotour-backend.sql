
    create table Activity (
       id int8 generated by default as identity,
        activityName varchar(255),
        comment varchar(255),
        isPublic boolean not null,
        latitude float8 not null,
        longitude float8 not null,
        publicationDate date,
        startDateTime timestamp,
        previousActivity_id int8,
        primary key (id)
    );

    create table Event (
       id int8 generated by default as identity,
        maxPersonAllowed int4 not null,
        name varchar(255),
        planedEndDateTime timestamp,
        planedStartDateTime timestamp,
        type varchar(255),
        primary key (id)
    );

    create table Event_Person (
       Event_id int8 not null,
        participant_id int8 not null
    );

    create table Event_Topic (
       Event_id int8 not null,
        topics_id int8 not null
    );

    create table Person (
       id int8 generated by default as identity,
        comment varchar(255),
        firstname varchar(255),
        lastname varchar(255),
        role int4,
        telephone varchar(255),
        primary key (id)
    );

    create table Topic (
       id int8 generated by default as identity,
        comment varchar(255),
        name varchar(255),
        previousTopic_id int8,
        primary key (id)
    );

    create table Topic_Activity (
       Topic_id int8 not null,
        activity_id int8 not null
    );

